    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        String id = txtid.getText();
        String nama = txtnamapetugas.getText();
        String alamat = txtalamat.getText();
        String email = txtemail.getText();
        String telepon = txttelp.getText();

// Memeriksa apakah ada field yang kosong
if (id.isEmpty() || nama.isEmpty() || alamat.isEmpty() || email.isEmpty() || telepon.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "error", JOptionPane.WARNING_MESSAGE);
} else {
    Connection c = null;
    PreparedStatement p = null;
    try {
        c = koneksi.getKoneksi();
        String sql = "INSERT INTO tblpetugas (IDPetugas, NamaPetugas, Alamat, Email, Telepon) VALUES (?, ?, ?, ?, ?)";
        p = c.prepareStatement(sql);

        p.setString(1, id);
        p.setString(2, nama);
        p.setString(3, alamat);
        p.setString(4, email);
        p.setString(5, telepon);

        // Menjalankan query untuk menyimpan data
        int rowsAffected = p.executeUpdate();
        if (rowsAffected > 0) {
            JOptionPane.showMessageDialog(null, "Penyimpanan data berhasil");
        } else {
            JOptionPane.showMessageDialog(null, "Gagal menyimpan data");
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    } finally {
        loaddata();
        kosong();
        setEnabletrue();
        btnadd.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
       int i = TablePetugas.getSelectedRow();
        
        if(i == -1){
            //tidak ada baris tewrseleksi
            JOptionPane.showMessageDialog(this, "Harap Pilih Data Terlebih Dahulu", "ERROR", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String id = (String) model.getValueAt(i, 0);
        
        try{
            Connection c = koneksi.getKoneksi();
            
            String sql = "DELETE FROM tblpetugas WHERE IDPetugas = ?";
            
            PreparedStatement p = c.prepareStatement(sql);
            p.setString(1, id);
            p.executeUpdate();
            p.close();
            
            JOptionPane.showMessageDialog(null, "Hapus Data Berhasil");
        }catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Terjadi ERROR");
        }finally {
            loaddata();
            kosong();
        }
    }//GEN-LAST:event_btndeleteActionPerformed

