    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        int i = TablePetugas.getSelectedRow();
        
        if(i == -1){
            //tidak ada baris tewrseleksi
            JOptionPane.showMessageDialog(this, "harap pilih data terlebih dahulu", "error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String id = (String) model.getValueAt(i, 0);
        String nama = txtnamapetugas.getText();
        String alamat = txtalamat.getText();
        String email = txtemail.getText();
        String telepon = txttelp.getText();
        
        try {
        Connection c = koneksi.getKoneksi();

        String sql = "UPDATE tblpetugas SET NamaPetugas = ?, Alamat = ?, Email = ?, Telepon = ? WHERE IDPetugas = ?"; // Pastikan nama tabel dan kolom yang benar

        PreparedStatement p = c.prepareStatement(sql);

        p.setString(1, nama);
        p.setString(2, alamat);
        p.setString(3, email);
        p.setString(4, telepon);
        p.setString(5, id);

        p.executeUpdate();
        p.close();

        JOptionPane.showMessageDialog(null, "Ubah Data Berhasil");
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Terjadi ERROR: " + e.getMessage());
    } finally {
        loaddata();
        kosong();
    }
    }//GEN-LAST:event_btnupdateActionPerformed

