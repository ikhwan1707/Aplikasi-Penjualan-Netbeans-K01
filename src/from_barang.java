    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        // TODO add your handling code here:
        String kode_barang = txtkodebarang.getText();
        String nama_barang = txtnamabarang.getText();
        String jenis_barang = txtjenisbarang.getText();
        String harga_net = txtharganet.getText();
        String harga_jual = txthargajual.getText();
        String stok = txtstok.getText();
        
        String KJ=combobarang.getSelectedItem().toString();
        if ("".equals(kode_barang) || 
                "".equals(nama_barang) ||
                "".equals(jenis_barang) || 
                "".equals(harga_net) ||
                "".equals(harga_jual) ||
                "".equals(stok)||"".equals(KJ))
        {
            
            JOptionPane.showMessageDialog(this, "Harap Lengkapi Data", "Error", JOptionPane.WARNING_MESSAGE);
        }else {
           
            try{
                Connection c = koneksi.getKoneksi();
                String sql = "INSERT INTO tblbarang VALUES (?, ?, ?, ?, ?, ?)";
                PreparedStatement p = c.prepareStatement(sql);
               
                
                p.setString(1, kode_barang);
                p.setString(2, nama_barang);
                p.setString(3, KJ);
                p.setString(4, harga_net);
                p.setString(5, harga_jual);
                p.setString(6, stok);
               
          

                p.executeUpdate();
                p.close();
                
                JOptionPane.showMessageDialog(null, "Berhasil ditambah");
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }finally {
                loadData();
                kosong();
               setEnablefalse();
               btnaddnew.setEnabled(true);
            }
        }
    }//GEN-LAST:event_btnsaveActionPerformed

