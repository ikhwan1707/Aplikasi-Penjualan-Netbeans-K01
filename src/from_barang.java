    private void tabelbarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelbarangMouseClicked
        // TODO add your handling code here:
        int baris = tabelbarang.getSelectedRow();
        if(baris == -1) {
            return ;
        }
        
        String kode_barang = tabelbarang.getValueAt(baris, 0).toString();
        txtkodebarang.setText(kode_barang);
        String nama_barang = tabelbarang.getValueAt(baris, 1).toString();
        txtnamabarang.setText(nama_barang);
        combobarang.setSelectedItem(tabelbarang.getValueAt(baris, 2).toString());
        String harga_net = tabelbarang.getValueAt(baris, 3).toString();
        txtharganet.setText(harga_net);
        String harga_jual = tabelbarang.getValueAt(baris, 4).toString();
        txthargajual.setText(harga_jual);
        String stok = tabelbarang.getValueAt(baris, 5).toString();
        txtstok.setText(stok);
    }//GEN-LAST:event_tabelbarangMouseClicked

