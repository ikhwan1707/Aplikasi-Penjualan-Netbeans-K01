    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
           int i = tabelbarang.getSelectedRow();
        
        if(i == -1){
            //tidak ada baris terseleksi
            JOptionPane.showMessageDialog(this, "harap pilih data terlebih dahulu", "error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String kode_barang = (String) model.getValueAt(i, 0);
        String nama_barang = txtnamabarang.getText();
        String KJ = combobarang.getSelectedItem().toString();
        String harga_net = txtharganet.getText();
        String harga_jual = txthargajual.getText();
        String stok = txtstok.getText();
        
        try{
            
            Connection c = koneksi.getKoneksi();
            
            String sql = "UPDATE tblbarang SET NamaBarang = ?,KodeJenis = ?, HargaNet = ?, HargaJual = ? , Stok = ? WHERE KodeBarang = ?";
            
            PreparedStatement p = c.prepareStatement(sql);
            
            
                
                p.setString(1, nama_barang);
                p.setString(2, KJ);
                p.setString(3, harga_net);
                p.setString(4, harga_jual);
                p.setString(5, stok);
                p.setString(6, kode_barang);
                
            p.executeUpdate();
                p.close();
                
                JOptionPane.showMessageDialog(null,
                       "Ubah Data Berhasil");
                
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this,
                        e.getMessage());
                
            }finally{
            loadData();
            kosong();
        }
    }//GEN-LAST:event_btnupdateActionPerformed

    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        // TODO add your handling code here:
        int i = tabelbarang.getSelectedRow();
        
        if(i == -1){
            JOptionPane.showMessageDialog(this, "Harap Pilih Data Terlebih Dahulu", "Error", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        String kode_barang = (String) model.getValueAt(i, 0);
        
        try{
            Connection c = koneksi.getKoneksi();
            
           String sql = "DELETE FROM tblbarang WHERE KodeBarang=?";
                     
                     PreparedStatement p = c.prepareStatement(sql);
                     p.setString(1, kode_barang);
                     p.executeUpdate();
                     p.close();
                     
                     JOptionPane.showMessageDialog(null, "Hapus Data Berhasil");
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Terjadi Error"+e.getMessage());
        } finally{
            loadData();
            kosong();
        }
           
    }//GEN-LAST:event_btndeleteActionPerformed

